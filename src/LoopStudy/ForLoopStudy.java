package LoopStudy;


public class ForLoopStudy {

	public static void main(String[] args) {
		
		String name="RR";
		String name1="KKR";
		String name2="MI";
		String name3="CSK";
		
		for (int i=1;i<=10;i++)
		{
			System.out.print(" "+name);
			System.out.print(" "+name1);
			System.out.print(" "+name2);
			System.out.println(" "+name3);
			
		}

	}

}
