package LoopStudy;

public class While_Study {

	public static void main(String[] args) {
		// we want to print 5 table
		//increment
		//6-60
	
		int n=6;// initialization
		
		while(n<=60)//condition
		{
			System.out.println(" "+n);
			
			n=n+6;//updation
		}

		//we want to print 100-50 in reverse
		//decrement
		//100-50
		
		int m=100;// initialization
		
		while(m>=50)
		{
			System.out.println(m);
			
			m=m-5;
			
		}
	}

}
